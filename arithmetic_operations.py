num_1 = 12
num_2 = 63

# Addition
addition = num_1 + num_2
print("The addition of {} and {} is {}".format(num_1, num_2, addition))

# Subtraction
subtraction = num_1 - num_2
print("The subtraction of {} and {} is {}".format(num_1, num_2, subtraction))

# Division
div = num_1 / num_2
print("The division of {} by {} is {}".format(num_1, num_2, div))

# Multiplication
mult = num_1 * num_2
print("The multiplication of {} and {} is {}".format(num_1, num_2, mult))
